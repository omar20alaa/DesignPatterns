package app.design_patterns.builder_pattern;

public class Definition {


    /*

    The builder pattern, as the name implies, is an alternative way to construct complex objects.
    This pattern should be used when we want to build different immutable
    objects using the same object building process.


    The builder pattern is a design pattern that allows for the step-by-step creation
    of complex objects using the correct sequence of actions.
    The construction is controlled by a director object that only needs to know the type of object it is to create.


    Builder pattern aims to “Separate the construction of a complex object from its representation
    so that the same
    construction process can create multiple different representations.”

     */


}
